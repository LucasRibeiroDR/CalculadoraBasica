package CalculadoraBasica;
/**
 *
 * @author Lucas
 */
public class CalculadoraBasica {
    float x;
    float y;
    
    public CalculadoraBasica(){
        x=0;
        y=0;
    }
    
    float somar(){
        
        return x+y;

    }
    
    float subtrair(){
        
        return x-y;
    }
    
    float dividir(){
        
            return x/y;
        
    }
    
    float multiplicar(){
        
        return x*y;
    }
}




